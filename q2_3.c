#include<stdio.h>
void main()
{
    int c=1;
    int i,j;
    int k=10;
    for(i=1;i<=k*2;i++)
    {
        for(j=1;j<=i;j++)
        {
            printf("*");
        }
        if(i<k)
        {
            c++;
        }
        else
        {
            c--;
        }
        printf("\n");
    }
}

